package com.sendbird.uikit.consts;

/**
 * Identifier to determine which area was clicked when {@code MessageViewHolder} was clicked
 */
public enum ClickableViewIdentifier {
    /**
     * Main message area
     */
    Chat,
    /**
     * Sender's profile area
     */
    Profile,
    /**
     * Quoted message area
     */
    QuoteReply,
    ;
}
